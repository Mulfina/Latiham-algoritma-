# Mendefinisikan variabel dengan tipe data integer dan float
angka1 = 10    # integer
angka2 = 3.5   # float

# Penjumlahan
hasil_tambah = angka1 + angka2
print("Hasil penjumlahan:", hasil_tambah)

# Pengurangan
hasil_kurang = angka1 - angka2
print("Hasil pengurangan:", hasil_kurang)

# Perkalian
hasil_kali = angka1 * angka2
print("Hasil perkalian:", hasil_kali)

# Pembagian
hasil_bagi = angka1 / angka2
print("Hasil pembagian:", hasil_bagi)

# Pembagian bulat
hasil_bagi_bulat = angka1 // angka2
print("Hasil pembagian bulat:", hasil_bagi_bulat)

# Modulus (sisa bagi)
hasil_modulus = angka1 % angka2
print("Hasil modulus:", hasil_modulus)

# Pangkat
hasil_pangkat = angka1 ** 2
print("Hasil pangkat:", hasil_pangkat)