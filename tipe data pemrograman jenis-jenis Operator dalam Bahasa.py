a = 10
b = 3

# Operator aritmatika
print("Penjumlahan:", a + b)      # Output: 13
print("Pengurangan:", a - b)      # Output: 7
print("Perkalian:", a * b)        # Output: 30
print("Pembagian:", a / b)        # Output: 3.3333
print("Modulus:", a % b)          # Output: 1
print("Eksponen:", a ** b)        # Output: 1000
print("Pembagian Bulat:", a // b) # Output: 3

x = 5
y = 10

# Operator perbandingan
print("Apakah x sama dengan y?", x == y)     # Output: False
print("Apakah x tidak sama dengan y?", x != y) # Output: True
print("Apakah x lebih kecil dari y?", x < y) # Output: True
print("Apakah x lebih besar dari y?", x > y) # Output: False
print("Apakah x kurang dari atau sama dengan y?", x <= y) # Output: True
print("Apakah x lebih dari atau sama dengan y?", x >= y) # Output: False

p = True
q = False

# Operator logika
print("p and q:", p and q)   # Output: False
print("p or q:", p or q)     # Output: True
print("not p:", not p)       # Output: False

n = 5
print("Nilai awal n:", n)   # Output: 5

n += 3     # Sama dengan n = n + 3
print("Setelah n += 3:", n) # Output: 8

n -= 2     # Sama dengan n = n - 2
print("Setelah n -= 2:", n) # Output: 6

n *= 4     # Sama dengan n = n * 4
print("Setelah n *= 4:", n) # Output: 24

n /= 3     # Sama dengan n = n / 3
print("Setelah n /= 3:", n) # Output: 8.0

n %= 5     # Sama dengan n = n % 5
print("Setelah n %= 5:", n) # Output: 3.0

c = 6    # Biner: 110
d = 2    # Biner: 010

# Operator bitwise
print("Bitwise AND:", c & d)      # Output: 2 (010)
print("Bitwise OR:", c | d)       # Output: 6 (110)
print("Bitwise XOR:", c ^ d)      # Output: 4 (100)
print("Bitwise NOT:", ~c)         # Output: -7 (001)
print("Left Shift:", c << 1)      # Output: 12 (1100)
print("Right Shift:", c >> 1)     # Output: 3 (011)