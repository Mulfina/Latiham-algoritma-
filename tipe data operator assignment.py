# Nilai awal
a = 10

# Operator assignment penjumlahan
a += 5  # Sama dengan a = a + 5
print("Hasil dari a += 5:", a)

# Operator assignment pengurangan
a -= 3  # Sama dengan a = a - 3
print("Hasil dari a -= 3:", a)

# Operator assignment perkalian
a *= 4  # Sama dengan a = a * 4
print("Hasil dari a *= 4:", a)

# Operator assignment pembagian
a /= 2  # Sama dengan a = a / 2
print("Hasil dari a /= 2:", a)

# Operator assignment modulus
a %= 3  # Sama dengan a = a % 3
print("Hasil dari a %= 3:", a)

# Operator assignment eksponensial
a **= 3  # Sama dengan a = a ** 3
print("Hasil dari a **= 3:", a)

# Operator assignment pembagian integer
a //= 2  # Sama dengan a = a // 2
print("Hasil dari a //= 2:", a)