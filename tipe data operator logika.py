# Definisi dua variabel boolean
x = True
y = False

# Operator Logika
print("x =", x, ", y =", y)  # Output nilai x dan y

# Operator AND
print("x and y :", x and y)  # False karena hanya x yang True, sedangkan y False

# Operator OR
print("x or y :", x or y)  # True karena salah satu (x) bernilai True

# Operator NOT
print("not x :", not x)  # False karena nilai x adalah True, lalu dibalik oleh not
print("not y :", not y)  # True karena nilai y adalah False, lalu dibalik oleh not`